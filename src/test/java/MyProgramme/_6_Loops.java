package MyProgramme;

public class _6_Loops {
	
	public static void main(String[] args) {
		
		//While Loop 
		
		int i = 1 ;
		while(10>=i)
			{
				//System.out.println(i);
				if(i==8)
				{
					System.out.println("eight");				
				}
			i++;
			}
		
		
		// For Loop
		
	for(int j=3; j<=30; j++)
	{
		if(j%3 == 0)			// printed 3 table 
		{
			System.out.println(j);
		}
	}
		
	}

}

