package MyProgramme;

public class _5_SwitchCase {

	public static void main(String[] args) {
		
		int a = 500;
		
		switch(a)
		{
		case 0 :
			System.out.println(10);
			break;
		case 1 :
			System.out.println(50);
			break;
		case 2:
			System.out.println(100);
			break;
		case 3:
			System.out.println(150);
			break;
			
		default:
			System.out.println("Not matched any no of given cases");
			break;

		}
	}

}
