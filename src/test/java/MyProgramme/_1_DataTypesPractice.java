package MyProgramme;

public class _1_DataTypesPractice {
	
	public static void main(String[] args) {
		
		// Primitive Data Types = Primitive data type means predefined by language and is named by a reserved keyword..
		//(int,byte,long,short,boolean,float,char.double)
		int a = 10;
		System.out.println(a);
		
		double b = 11.1111; //double value are goes in point value
		System.out.println(b);
		
		short s = 100;
		System.out.println(s);
		
		float f = 12.34f;//when we use float .we have assign value.and compulsory last we have add f letter
		System.out.println(f);
		
		boolean q = true; // true = 1 and false = 0
		System.out.println(q);
		
		char d = 'M'; 
		System.out.println(d);
		
		// Non Primitive Data Types = called as reference types because they refer to objects.
		
		String g = "Manali";
		System.out.println(g);
		
		
		
	}

}
